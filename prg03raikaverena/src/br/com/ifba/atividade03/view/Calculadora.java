    private void bntCalculaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bntCalculaActionPerformed
     //Pega o ano de nascimento (string) e converte para inteiro
        int anoNascimento = Integer.parseInt(txtNascimento.getText());
        //LocalDate.now() cria um objeto LocalDate com a data atual do sistema
        //getYear() pega apenas o ano desse objeto
        int anoAtual = LocalDate.now().getYear();
        //calcula a idade
        int idade = anoAtual - anoNascimento;
        //modifica o resultado, mostrando a idade atual
        lblResultado.setText(idade + "anos");
    }//GEN-LAST:event_bntCalculaActionPerformed

